package ej5;

public class Persona {
	
	private String nom;
	private int edad;
	private String sexe;
	
	public Persona(String nom, int edad, String sexe) {
		super();
		this.nom = nom;
		this.edad = edad;
		this.sexe = sexe;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getSexe() {
		return sexe;
	}

	public void setSexe(String sexe) {
		this.sexe = sexe;
	}
	
	
	
	
	
	

}
